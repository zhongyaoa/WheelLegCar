#ifndef _ZF_DEVICE_PMW3901_H_
#define _ZF_DEVICE_PMW3901_H_

#include "zf_common_typedef.h"

//====================================================硬件 SPI 驱动====================================================
#define PMW3901_SPI_SPEED            (2 * 1000 * 1000 )                         // 硬件 SPI 速率
#define PMW3901_SPI                  (SPI_0           )                         // 硬件 SPI 号
#define PMW3901_SCLK_PIN             (SPI0_CLK_P02_2  )                         // 硬件 SPI SCK 引脚
#define PMW3901_MOSI_PIN             (SPI0_MOSI_P02_1 )                         // 硬件 SPI MOSI 引脚
#define PMW3901_MISO_PIN             (SPI0_MISO_P02_0 )                         // 硬件 SPI MISO 引脚
#define PMW3901_CS_PIN               (P02_3)                                    // CS 片选引脚
#define PMW3901_CS(x)                ((x) ? (gpio_high(PMW3901_CS_PIN)) : (gpio_low(PMW3901_CS_PIN)))
//====================================================硬件 SPI 驱动====================================================



//================================================定义 PMW3901 内部地址================================================
#define PMW3901_PRODUCT_ID           (0x00)
#define PMW3901_REVISION_ID          (0x01)
#define PMW3901_MOTION               (0x02)
#define PMW3901_DELTA_X_L            (0x03)
#define PMW3901_DELTA_X_H            (0x04)
#define PMW3901_DELTA_Y_L            (0x05)
#define PMW3901_DELTA_Y_H            (0x06)
#define PMW3901_MOTION_BURST         (0x16)
#define PMW3901_POWER_UP_RESET       (0x3A)
#define PMW3901_SHUTDOWN             (0x3B)
#define PMW3901_OBSERVATION          (0x15)
//================================================定义 PMW3901 内部地址================================================



//================================================声明 PMW3901 全局变量================================================
extern int16 pmw3901_delta_x  , pmw3901_delta_y  ;                              // 偏移速度数据变量
extern int32 pmw3901_delta_x_i, pmw3901_delta_y_i;                              // 偏移距离数据变量
//================================================声明 PMW3901 全局变量================================================



//================================================声明 PMW3901 基础函数================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     初始化 PMW3901
// 参数说明     void
// 返回参数     uint8           1-失败 0-成功
// 使用示例     pmw3901_init();     
// 备注信息         
//-------------------------------------------------------------------------------------------------------------------
uint8 pmw3901_init              (void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取 PMW3901 运动数据
// 参数说明     void
// 返回参数     void
// 使用示例     pmw3901_get_motion();
// 备注信息     执行后直接查看 pmw3901_delta_x 和 pmw3901_delta_y  
//              请务必注意以下使用事项：
//              1.该函数调用周期至少 20 毫秒
//              2.测量距离至少大于 5 厘米
//              3.务必保持光线充足 否则无法正常采集到光流数据
//-------------------------------------------------------------------------------------------------------------------
void  pmw3901_get_motion        (void);

//================================================声明 PMW3901 基础函数================================================

#endif
